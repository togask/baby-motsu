-- 注意
-- ユーザーテーブルと配送先テーブルが相互参照をしています。
-- テーブル作成後にユーザーテーブルのactive_shipping_address_idを更新してください。
-- コードマスタ
CREATE TABLE CODE_MASTER (
  code_id INT PRIMARY KEY AUTO_INCREMENT,
  table VARCHAR(255) NOT NULL,
  name VARCHAR(255) NOT NULL,
);

-- ユーザー
CREATE TABLE `USER` (
  user_id INT PRIMARY KEY AUTO_INCREMENT,
  email VARCHAR(255) UNIQUE,
  password VARCHAR(255) NOT NULL,
  nickname VARCHAR(100) NOT NULL,
  profile_image_path VARCHAR(500) DEFAULT 'default_profile.png',
  introduce TEXT,
  active_shipping_address_id INT,
  name VARCHAR(100),
  name_kana VARCHAR(100),
  birthday DATE,
  address TEXT,
  point INT DEFAULT 0,
  session_id VARCHAR(255)
);

-- 配送先
CREATE TABLE SHIPPING_ADDRESS (
  shipping_address_id INT PRIMARY KEY AUTO_INCREMENT,
  user_id INT NOT NULL,
  sei VARCHAR(50) NOT NULL,
  mei VARCHAR(50) NOT NULL,
  sei_kana VARCHAR(50) NOT NULL,
  mei_kana VARCHAR(50) NOT NULL,
  post_code VARCHAR(7) NOT NULL,
  prefectures VARCHAR(4) NOT NULL,
  municipalities VARCHAR(100) NOT NULL,
  street VARCHAR(100) NOT NULL,
  building_name VARCHAR(150),
  tel VARCHAR(15) NOT NULL,
  FOREIGN KEY(user_id) REFERENCES USER(user_id)
);

-- 大カテゴリ
CREATE TABLE MAJOR_CATEGORY (
  major_category_id INT PRIMARY KEY AUTO_INCREMENT,
  major_category VARCHAR(255) NOT NULL
);

-- 小カテゴリ
CREATE TABLE MINOR_CATEGORY (
  minor_category_id INT PRIMARY KEY AUTO_INCREMENT,
  major_category_id INT NOT NULL,
  minor_category VARCHAR(255) NOT NULL,
  FOREIGN KEY(major_category_id) REFERENCES MAJOR_CATEGORY(major_category_id)
);

-- 色
CREATE TABLE COLOR (
  color_id INT PRIMARY KEY AUTO_INCREMENT,
  color VARCHAR(255) NOT NULL,
  color_code VARCHAR(7) NOT NULL
);

-- 商品
CREATE TABLE PRODUCT (
  product_id INT PRIMARY KEY AUTO_INCREMENT,
  seller_id INT NOT NULL,
  age_id INT NOT NULL,
  weight_id INT NOT NULL,
  height_id INT NOT NULL,
  major_category_id INT NOT NULL,
  minor_category_id INT NOT NULL,
  bland_id INT NOT NULL,
  color_id INT NOT NULL,
  product_condition_id INT NOT NULL,
  shipping_fee_responsibility_id INT NOT NULL,
  shipping_method_id INT NOT NULL,
  shipping_origin_region_id INT NOT NULL,
  day_to_ship_id INT NOT NULL,
  status_id INT NOT NULL,
  product_name VARCHAR(40) NOT NULL,
  product_description TEXT NOT NULL,
  price INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(seller_id) REFERENCES USER(user_id),
  FOREIGN KEY(age_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(weight_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(height_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(major_category_id) REFERENCES MAJOR_CATEGORY(major_category_id),
  FOREIGN KEY(minor_category_id) REFERENCES MINOR_CATEGORY(minor_category_id),
  FOREIGN KEY(bland_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(color_id) REFERENCES COLOR(color_id),
  FOREIGN KEY(product_condition_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(shipping_fee_responsibility_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(shipping_method_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(shipping_origin_region_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(day_to_ship_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(status_id) REFERENCES CODE_MASTER(code_id)
);

-- 商品画像
CREATE TABLE PRODUCT_IMAGE (
  product_image_id INT PRIMARY KEY AUTO_INCREMENT,
  product_id INT NOT NULL,
  path VARCHAR(500) NOT NULL,
  `order` INT NOT NULL,
  FOREIGN KEY(product_id) REFERENCES PRODUCT(product_id)
);

-- お気に入り
CREATE TABLE FAVORITE (
  product_id INT NOT NULL,
  user_id INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (product_id, user_id),
  FOREIGN KEY(product_id) REFERENCES PRODUCT(product_id),
  FOREIGN KEY(user_id) REFERENCES USER(user_id)
);

-- 閲覧
CREATE TABLE PRODUCTVIEWS (
  product_id INT NOT NULL,
  user_id INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (product_id, user_id),
  FOREIGN KEY(product_id) REFERENCES PRODUCT(product_id),
  FOREIGN KEY(user_id) REFERENCES USER(user_id)
);

-- 検索履歴
CREATE TABLE SEARCH_HISTORY (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL FOREIGN KEY REFERENCES USER(user_id),
  search_query VARCHAR(255),
  search_params TEXT,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 取引
CREATE TABLE TRANSACTION (
  transaction_id INT PRIMARY KEY AUTO_INCREMENT,
  buyer_id INT NOT NULL,
  seller_id INT NOT NULL,
  product_id INT NOT NULL,
  transaction_status_id INT NOT NULL,
  amount INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(buyer_id) REFERENCES USER(user_id),
  FOREIGN KEY(seller_id) REFERENCES USER(user_id),
  FOREIGN KEY(product_id) REFERENCES PRODUCT(product_id),
  FOREIGN KEY(transaction_status_id) REFERENCES CODE_MASTER(code_id)
);

-- 評価
CREATE TABLE EVALUATION (
  evalution_id INT PRIMARY KEY AUTO_INCREMENT,
  transaction_id INT NOT NULL,
  evaluator_id INT NOT NULL,
  evaluatee_id INT NOT NULL,
  is_seller BOOLEAN NOT NULL,
  comment TEXT,
  score INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(transaction_id) REFERENCES TRANSACTION(transaction_id),
  FOREIGN KEY(evaluator_id) REFERENCES USER(user_id),
  FOREIGN KEY(evaluatee_id) REFERENCES USER(user_id)
);

-- ポイント
CREATE TABLE POINT (
  point_id INT PRIMARY KEY AUTO_INCREMENT,
  user_id INT NOT NULL,
  transaction_id INT NOT NULL,
  variable_point INT NOT NULL,
  is_plus BOOLEAN NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(user_id) REFERENCES USER(user_id),
  FOREIGN KEY(transaction_id) REFERENCES TRANSACTION(transaction_id)
);

-- レビュー
CREATE TABLE REVIEW (
  review_id INT PRIMARY KEY AUTO_INCREMENT,
  product_id INT NOT NULL,
  buyer_id INT NOT NULL,
  usage_duration_id INT NOT NULL,
  comment TEXT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(product_id) REFERENCES PRODUCT(product_id),
  FOREIGN KEY(buyer_id) REFERENCES USER(user_id),
  FOREIGN KEY(usage_duration_id) REFERENCES CODE_MASTER(code_id)
);

-- チャージ
CREATE TABLE CHARGE (
  charge_id INT PRIMARY KEY AUTO_INCREMENT,
  user_id INT NOT NULL,
  point INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(user_id) REFERENCES USER(user_id)
);

-- お知らせ
CREATE TABLE NOTICE (
  notice_id INT PRIMARY KEY AUTO_INCREMENT,
  user_id INT NOT NULL,
  transaction_id INT NOT NULL,
  type_id INT NOT NULL,
  notice_status_id INT NOT NULL,
  datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY(user_id) REFERENCES USER(user_id),
  FOREIGN KEY(transaction_id) REFERENCES TRANSACTION(transaction_id),
  FOREIGN KEY(type_id) REFERENCES CODE_MASTER(code_id),
  FOREIGN KEY(notice_status_id) REFERENCES CODE_MASTER(code_id)
);

